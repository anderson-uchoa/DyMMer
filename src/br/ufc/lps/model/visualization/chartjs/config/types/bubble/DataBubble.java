package br.ufc.lps.model.visualization.chartjs.config.types.bubble;

import br.ufc.lps.model.visualization.chartjs.config.DataSet;

public class DataBubble extends DataSet{
	private Double x;
	private Double y;
	private Double r;
	
	public Double getX() {
		return x;
	}
	public void setX(Double x) {
		this.x = x;
	}
	public Double getY() {
		return y;
	}
	public void setY(Double y) {
		this.y = y;
	}
	public Double getR() {
		return r;
	}
	public void setR(Double r) {
		this.r = r;
	}
	
	
}
